package com.upskill.basics.java.basics.branching;

public class Task5Formula {
    public static void main(String[] args) {
        double x = 1;

        if (x <= 3) {
            System.out.println(x * x - 3 * x + 9);
        } else {
            System.out.println(1 / (x * x * x + 6));
        }
    }
}
